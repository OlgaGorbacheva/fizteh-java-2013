package ru.fizteh.fivt.students.mishatkin.multifilehashmap;

import ru.fizteh.fivt.students.mishatkin.shell.ShellException;

/**
 * Created by Vladimir Mishatkin on 10/26/13
 */
public class MultiFileHashMapException extends ShellException {
	public MultiFileHashMapException(String s) {
		super(s);
	}

	public MultiFileHashMapException() {
		super();
	}

	public MultiFileHashMapException(String message, Throwable cause) {
		super(message, cause);
	}

	public MultiFileHashMapException(Throwable cause) {
		super(cause);
	}
}
